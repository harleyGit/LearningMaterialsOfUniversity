package org.bzc.jpetstore.beans;

public class Sequence {
	private String name;

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	private Integer nextid;

	public void setNextid(Integer nextid) {
		this.nextid = nextid;
	}

	public Integer getNextid() {
		return nextid;
	}
}
